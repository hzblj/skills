#!/usr/bin/env node
// Generate an agents/openai.yaml next to every SKILL.md under skills/.
// This is the OpenAI Codex / ChatGPT skill config — optional metadata that gives
// the skill a nicer name in the ChatGPT desktop app and sets its invocation policy.
// Claude Code and skills.sh->Claude ignore it; it only matters on OpenAI harnesses.
// Derived from each SKILL.md's frontmatter, so it never drifts. Run with `yarn openai-yaml`.
// Docs: https://learn.chatgpt.com/docs/build-skills
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const REPO = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const SKILLS = path.join(REPO, "skills");

// Per-word casing overrides so display names read right (UI, GSAP, CSS, Next.js).
const WORD = { ui: "UI", gsap: "GSAP", css: "CSS", nextjs: "Next.js", and: "and" };

const displayName = (name) =>
  name
    .split("-")
    .map((w) => WORD[w] ?? w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");

const listDirs = (dir) =>
  fs
    .readdirSync(dir, { withFileTypes: true })
    .filter((e) => e.isDirectory())
    .map((e) => path.join(dir, e.name))
    .sort();

const hasSkill = (dir) => fs.existsSync(path.join(dir, "SKILL.md"));

// Stop descending once inside a leaf skill — we only want the skill roots.
const skillDirs = (dir) => {
  if (hasSkill(dir)) return [dir];
  return listDirs(dir).flatMap(skillDirs);
};

const readFrontmatter = (file) => {
  const txt = fs.readFileSync(file, "utf8");
  const m = txt.match(/^---\s*\n([\s\S]*?)\n---/);
  if (!m) return { name: "", description: "" };
  const block = m[1];
  const nameM = block.match(/^name:\s*(.+)$/m);
  const descM = block.match(/^description:\s*(.*)$/m);
  let description = "";
  if (descM) {
    const first = descM[1].trim();
    if (["|", ">", "|-", ">-", ""].includes(first)) {
      // block scalar — gather following indented lines
      const after = block.slice(descM.index + descM[0].length).split("\n");
      const lines = [];
      for (const ln of after) {
        if (ln.trim() === "" && lines.length === 0) continue;
        if (/^\s+\S/.test(ln) || ln.trim() === "") lines.push(ln.trim());
        else break;
      }
      description = lines.filter(Boolean).join(" ");
    } else {
      description = first;
    }
  }
  return { name: nameM ? nameM[1].trim() : "", description };
};

// Keep the "what — details" lead; drop the "Use ..." / "Triggers on:" tails.
// No sentence-splitting (would break on "vs." / "e.g.").
const short = (desc) => {
  let s = desc.split(/\bTriggers on:/)[0].split(/\.\s+Use\b/)[0];
  s = s.trim().replace(/\.+$/, "").trim();
  return s.length > 140 ? s.slice(0, 137).trimEnd() + "…" : s;
};

// JSON strings are valid YAML double-quoted scalars — reuse them for safe escaping.
const q = (s) => JSON.stringify(s);

const gen = (name, description) =>
  [
    "# Generated by scripts/gen-openai-yaml.mjs — do not edit by hand.",
    "# OpenAI Codex / ChatGPT skill config. Docs: https://learn.chatgpt.com/docs/build-skills",
    "interface:",
    `  display_name: ${q(displayName(name))}`,
    `  short_description: ${q(short(description))}`,
    "policy:",
    "  allow_implicit_invocation: true",
    "",
  ].join("\n");

const written = [];
for (const dir of skillDirs(SKILLS)) {
  const fm = readFrontmatter(path.join(dir, "SKILL.md"));
  const name = fm.name || path.basename(dir);
  const agentsDir = path.join(dir, "agents");
  fs.mkdirSync(agentsDir, { recursive: true });
  const out = path.join(agentsDir, "openai.yaml");
  fs.writeFileSync(out, gen(name, fm.description));
  written.push(path.relative(REPO, out));
}

console.log(`generated ${written.length} openai.yaml files`);
for (const w of written.sort()) console.log("  " + w);
